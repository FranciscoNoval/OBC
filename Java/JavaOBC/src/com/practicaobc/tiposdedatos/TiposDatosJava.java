package com.practicaobc.tiposdedatos;

public class TiposDatosJava {
    public static void main(String[] args) {

        //1
        //1.1 Enteros::.
        byte variable1 = 2;
        short variable2 = 40;
        int variable3 = 800;
        long variable4 = 16000;

        //1.2 Decimales::.
        float variable5 = 3.5f;
        double variable6 = 54.67d;

        //2 Booleano
        boolean variable7 = true;
        boolean variable8 = false;

        //3 Texto
        char variable9 ='a';
        String variable10 = "El que busca, encuentra.";

        System.out.println("Variable 1: "+variable1);
        System.out.println("Variable 2: "+variable2);
        System.out.println("Variable 3: "+variable3);
        System.out.println("Variable 4: "+variable4);
        System.out.println("Variable 5: "+variable5);
        System.out.println("Variable 6: "+variable6);
        System.out.println("Variable 7: "+variable7);
        System.out.println("Variable 8: "+variable8);
        System.out.println("Variable 9: "+variable9);
        System.out.println("Variable 10: "+variable10);
    }
}
